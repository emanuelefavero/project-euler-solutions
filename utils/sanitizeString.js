// * This function is useful for sanitizing the HTML text copied from Project Euler website
// TIP: For some reason project euler prevents numbers and other characters from being copied directly

function sanitize(string) {
  // Remove HTML tags
  const noTags = string.replace(/<[^>]*>/g, '')

  // Remove dollar signs
  const noDollarSign = noTags.replace(/\$/g, '')

  // Replace 'dots' with '...'
  const withDots = noDollarSign.replace(/dots/g, '...')

  return withDots
}

